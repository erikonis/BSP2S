class ColorCountResult {

	private int r = 0;
	private int b = 0;
	private int g = 0;
	
	//r b g??????????????????????????????????????????
	public void colorCount(String color) {
		if (color.equals("R")) {
			r++;
		} else if (color.equals("B")) {
			b++;
		} else if (color.equals("G")) {
			g++;
		}
	}

	// color?????\??????
	public boolean isColorResult() {
		// ????????????????????????????????°??§????????¨???true?????????
		return (r % 3 == 0) & (b % 3 == 0) & (g % 3 == 0);
	}

}

